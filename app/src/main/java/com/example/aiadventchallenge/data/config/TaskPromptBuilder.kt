    fun parseEnhancedAiResponse(response: String): EnhancedTaskAiResponse {
        val taskIntent = extractTaskIntent(response)
        val stepCompleted = response.contains("step_completed: true", ignoreCase = true)
        val taskCompleted = response.contains("task_completed: true", ignoreCase = true)
        val planReady = response.contains("plan_ready: true", ignoreCase = true)
        val nextAction = extractField(response, "next_action") ?: ""
        val result = extractResult(response)
        val transitionTo = extractField(response, "transition_to")?.let { phaseName ->
            try {
                TaskPhase.valueOf(phaseName.uppercase())
            } catch (e: Exception) {
                null
            }
        }
        val newTaskQuery = extractField(response, "new_task_query")
        val needClarification = extractField(response, "need_clarification")
        val errorMessage = extractField(response, "error")

        if (taskCompleted) {
            android.util.Log.d("TaskPromptBuilder", "✅ task_completed: true detected - will trigger DONE transition")
        }

        // Логирование если результат пустой
        if (result.isEmpty()) {
            android.util.Log.w("TaskPromptBuilder", "⚠️ Empty result detected - will use error message for UI")
        }

        return EnhancedTaskAiResponse(
            taskIntent = taskIntent,
            stepCompleted = stepCompleted,
            nextAction = nextAction,
            result = result,
            transitionTo = transitionTo,
            newTaskQuery = newTaskQuery,
            pauseTask = taskIntent == TaskIntent.PAUSE_TASK,
            needClarification = needClarification,
            errorMessage = errorMessage,
            taskCompleted = taskCompleted,
            planReady = planReady
        )
    }

    private fun extractTaskIntent(response: String): TaskIntent {
        val intentField = extractField(response, "task_intent") ?: return TaskIntent.CONTINUE_TASK
        return try {
            TaskIntent.valueOf(intentField.uppercase())
        } catch (e: Exception) {
            TaskIntent.CONTINUE_TASK
        }
    }

    private fun extractField(text: String, fieldName: String): String? {
        val patterns = listOf(
            Regex("\\*\\*$fieldName\\*\\*\\s*:\\s*([^\n]+)"),
            Regex("$fieldName\\s*:\\s*([^\n]+)")
        )
        for (pattern in patterns) {
            val match = pattern.find(text)
            if (match != null) {
                return match.groupValues.get(1).trim().removeSurrounding("\"")
            }
        }
        return null
    }

    private fun extractResult(response: String): String {
        // Сначала пробуем найти текст до основных метаданных
        var result = response.substringBefore("task_intent:").trim()

        // Если пусто, пробуем другие разделители (JSON, другие метаданные)
        if (result.isEmpty()) {
            val contentPatterns = listOf(
                Regex("""\{[\s\S]*?\}[\s\S]*?(?=(?:task_intent|step_completed|next_action|transition_to))"""),
                Regex("""[\s\S]*?(?=(?:task_intent|step_completed|next_action|transition_to))""")
            )

            for (pattern in contentPatterns) {
                val match = pattern.find(response)
                if (match != null && match.value.trim().isNotEmpty()) {
                    result = match.value.trim()
                    break
                }
            }
        }

        // Удаляем метаданные из результата (они не должны отображаться в чате)
        val metadataPatterns = listOf(
            Regex("""(?m)^step_completed\s*:\s*(true|false)\s*$"""),
            Regex("""(?m)^next_action\s*:\s*"[^"]*"\s*$"""),
            Regex("""(?m)^transition_to\s*:\s*(PLANNING|EXECUTION|VALIDATION|DONE|null)\s*$"""),
            Regex("""(?m)^task_completed\s*:\s*(true|false)\s*$"""),
            Regex("""(?m)^task_intent\s*:\s*(NEW_TASK|CONTINUE_TASK|SWITCH_TASK|PAUSE_TASK|CLARIFICATION)\s*$"""),
            Regex("""(?m)^new_task_query\s*:\s*"[^"]*"\s*$"""),
            Regex("""(?m)^need_clarification\s*:\s*"[^"]*"\s*$"""),
            Regex("""(?m)^error\s*:\s*"[^"]*"\s*$""")
        )

        val originalResult = result
        for (pattern in metadataPatterns) {
            result = pattern.replace(result, "").trim()
        }

        if (originalResult != result) {
            android.util.Log.d("TaskPromptBuilder", "✅ Removed metadata from response")
        }

        // Логируем если метаданные остались
        val metadataKeywords = listOf("step_completed:", "next_action:", "transition_to:", "task_completed:", "task_intent:", "new_task_query:", "need_clarification:")
        val hasRemainingMetadata = metadataKeywords.any { it in result }
        if (hasRemainingMetadata) {
            android.util.Log.w("TaskPromptBuilder", "⚠️ Warning: Metadata still present in result after cleanup")
            android.util.Log.w("TaskPromptBuilder", "   Result: $result")
        }

        // Логируем если текст ответа не найден
        if (result.isEmpty()) {
            android.util.Log.w("TaskPromptBuilder", "⚠️ Could not extract text from LLM response")
            android.util.Log.w("TaskPromptBuilder", "   Raw response: ${response.take(500)}")
            android.util.Log.w("TaskPromptBuilder", "   Returning empty string - UI will show error message")
        }

        return result
    }

